package base01.oop;

public class Star {
    private String name;
    private int age;

    public Star(){}

    public Star(String name,int age){
        this.name=name;
        this.age=age;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}
